package ex_19062024;

public class Lab014 {

    //pre post increment



    public static void main(String[] args) {

        int q=10;

        System.out.println(++q);
        System.out.println(q);

        System.out.println(q++);
        System.out.println(q);

        System.out.println(++q  + ++q);

        int y=10;

        System.out.println(++y + y++ + y++);

        System.out.println(y);


    }

}
